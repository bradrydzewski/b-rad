// IsPublic returns true if the email is public.
func IsPublic(s string) bool {
	s = extractDomain(s)
	_, ok := publicEmail[s]
	return ok
}

// IsThrowaway returns true if the email is throwaway.
func IsThrowaway(s string) bool {
	s = extractDomain(s)
	_, ok := throwawayEmail[s]
	return ok
}

// helper function extracts the domain from the email.
func extractDomain(s string) string {
	// normalize the string
	s = strings.ToLower(s)
	s = strings.TrimSpace(s)

	// split the email into segments to extract the
	// hostname.
	segments := strings.SplitN(s, "@", 2)
	if len(segments) == 2 {
		s = segments[1]
	}
	return s
}

